import Foundation
import UIKit

public extension CGColor {
    
    static let black = UIColor(white: 0.0, alpha: 1.0).cgColor
    static let darkGray = UIColor(white: 0.333, alpha: 1.0).cgColor
    static let lightGray = UIColor(white: 0.667, alpha: 1.0).cgColor
    static let white = UIColor(white: 1.0, alpha: 1.0).cgColor
    static let gray = UIColor(white: 0.5, alpha: 1.0).cgColor
    static let red = UIColor(red: 1.0, green: 0.0, blue: 0.0, alpha: 1.0).cgColor
    static let green = UIColor(red: 0.0, green: 1.0, blue: 0.0, alpha: 1.0).cgColor
    static let blue = UIColor(red: 0.0, green: 0.0, blue: 1.0, alpha: 1.0).cgColor
    static let cyan = UIColor(red: 0.0, green: 1.0, blue: 1.0, alpha: 1.0).cgColor
    static let yellow = UIColor(red: 1.0, green: 1.0, blue: 0.0, alpha: 1.0).cgColor
    static let magenta = UIColor(red: 1.0, green: 0.0, blue: 1.0, alpha: 1.0).cgColor
    static let orange = UIColor(red: 1.0, green: 0.5, blue: 0.0, alpha: 1.0).cgColor
    static let purple = UIColor(red: 0.5, green: 0.0, blue: 0.5, alpha: 1.0).cgColor
    static let brown = UIColor(red: 0.6, green: 0.4, blue: 0.2, alpha: 1.0).cgColor
    static let clear = UIColor(white: 0.0, alpha: 0.0).cgColor

}
